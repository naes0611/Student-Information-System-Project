    private void setupDividerPanel() {
        dividerPanel.setVisible(false);
        dividerPanel.setFocusable(true);
        dividerPanel.setFocusCycleRoot(true);
        MouseAdapter mouseBlocker = new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                e.consume();
            }
            
            @Override
            public void mousePressed(MouseEvent e) {
                e.consume();
            }
            
            @Override
            public void mouseReleased(MouseEvent e) {
                e.consume();
            }
        };
        dividerPanel.addMouseListener(mouseBlocker);
    }
    
    private void enableDividerPanel() {
        usernameTextField.setFocusable(false);
        passwordTextField.setFocusable(false);
        showPassword.setFocusable(false);
        signInButton.setFocusable(false);
        dividerPanel.setVisible(true);
        dividerPanel.requestFocusInWindow();
    } 
    
    private void disableDividerPanel() {
        usernameTextField.setFocusable(true);
        passwordTextField.setFocusable(true);
        showPassword.setFocusable(true);
        signInButton.setFocusable(true);
        dividerPanel.setVisible(false);
    }
