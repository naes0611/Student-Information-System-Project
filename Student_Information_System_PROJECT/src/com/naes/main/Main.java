    private void signInButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signInButtonActionPerformed
        if (dividerPanel.isVisible()) {
            return;
        }
        
        ValidationResult validationResult = validateLogin();
        
        if(!validationResult.isValid()) {
            showMessage(validationResult.getErrorMessage(), "warning");
            return;
        }
        
        signInButton.setText("Signing in");
        showLoading("Signing in, please wait");
        String username = usernameTextField.getText().trim();
        String password = String.valueOf(passwordTextField.getPassword());
        
        loginInBackground(username, password);
    }//GEN-LAST:event_signInButtonActionPerformed
    
    private void togglePasswordVisibility() {
        if (showPassword.isSelected()) {
            passwordTextField.setEchoChar((char) 0);
        } else {
            passwordTextField.setEchoChar('*');
        }
    }
    
    private void showPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showPasswordActionPerformed
        if (dividerPanel.isVisible()) {
            return;
        }
        togglePasswordVisibility();
    }//GEN-LAST:event_showPasswordActionPerformed
    
    private void addEnterKeyListener() {
        java.awt.event.KeyAdapter enterKeyListener = new java.awt.event.KeyAdapter() {
            @Override
            public void keyPressed(java.awt.event.KeyEvent evt) {
                if (evt.getKeyCode() == java.awt.event.KeyEvent.VK_ENTER) {
                    signInButton.doClick();
                }
            }
        };
        usernameTextField.addKeyListener(enterKeyListener);
        passwordTextField.addKeyListener(enterKeyListener);
    }
    
