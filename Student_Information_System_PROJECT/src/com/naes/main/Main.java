    private void showLoading(String message) {
        loadingBaseMessage = message;
        dotCount = 0;
        isLoading = true;
        
        enableDividerPanel();
        
        messageLabel.setIcon(null);
        messageLabel.setForeground(new java.awt.Color(51, 102, 255));
        messageLabel.setText(message + ".");
        messageLabel.setPreferredSize(new Dimension(MESSAGE_LABEL_WIDTH, MESSAGE_LABEL_HEIGHT));
        messageLabel.setVisible(true);
        resizeLoginContainer();
        
        loadingTimer = new javax.swing.Timer(500, e -> {
            if (isLoading) {
                dotCount = (dotCount % 3) + 1;
                String dots = ".".repeat(dotCount);
                messageLabel.setText(loadingBaseMessage + dots);
            }
        });
        loadingTimer.start();
    }
    
    private void hideLoading() {
        isLoading = false;
        
        disableDividerPanel();
        
        if (loadingTimer != null) {
            loadingTimer.stop();
            loadingTimer = null;
        }

        dotCount = 0;
        loadingBaseMessage = "";
        messageLabel.setForeground(new java.awt.Color(0, 0, 0)); // Reset to red for errors
    }
