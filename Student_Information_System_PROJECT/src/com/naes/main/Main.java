    private void loadImage(String image) {
        
        ImageIcon imageIcon;
        try {
            switch(image) {
                case "warning" -> { 
                    imageIcon = new ImageIcon(getClass().getResource("/com/naes/icons/caution.png")); 
                    messageLabel.setForeground(new java.awt.Color(0, 0, 0));
                }
                case "error" -> { 
                    imageIcon = new ImageIcon(getClass().getResource("/com/naes/icons/error.png"));
                    messageLabel.setForeground(new java.awt.Color(204, 0, 0)); 
                }
                default -> { imageIcon = null;
                    messageLabel.setForeground(new java.awt.Color(0, 0, 0));
                }
            }
            
            messageLabel.setIcon(imageIcon);
        } catch (Exception e) {
            logger.log(java.util.logging.Level.WARNING, "Could not load icon: " + image, e);
        }
    }
    
    private void showMessage(String message, String icon) {
        hideLoading();
        loadImage(icon);
        messageLabel.setText(message);
        messageLabel.setPreferredSize(new Dimension(MESSAGE_LABEL_WIDTH, MESSAGE_LABEL_HEIGHT));
        messageLabel.setVisible(true);
        resizeLoginContainer();
    }
    
    private void hideMessage() {
        hideLoading();
        messageLabel.setVisible(false);
        messageLabel.setPreferredSize(new Dimension(MESSAGE_LABEL_WIDTH, 0));
        resetLoginContainerSize();
    }
