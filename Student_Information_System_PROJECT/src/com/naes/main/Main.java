    // <editor-fold defaultstate="collapsed" desc="Container Methods">
    private void resizeLoginContainer() {
        Dimension currentSize = loginContainer.getPreferredSize();
        loginContainer.setPreferredSize(new Dimension(currentSize.width, originalLoginContainerHeight + MESSAGE_LABEL_HEIGHT));
        refreshLoginContainer();
    }
    
    private void resetLoginContainerSize() {
        Dimension currentSize = loginContainer.getPreferredSize();
        loginContainer.setPreferredSize(new Dimension(currentSize.width, originalLoginContainerHeight));
        refreshLoginContainer();
    }
    
    private void refreshLoginContainer() {
        loginContainer.revalidate();
        loginContainer.repaint();
    }
