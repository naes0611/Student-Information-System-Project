    private ValidationResult validateLogin() {
        String username = usernameTextField.getText().trim();
        String password = String.valueOf(passwordTextField.getPassword());
        
        if (username.isEmpty() && password.isEmpty()) {
            usernameTextField.grabFocus();
            return ValidationResult.failed("Please fill out the missing fields.");
        }
        
        if (!username.isEmpty() && password.isEmpty()) {
            passwordTextField.grabFocus();
            return ValidationResult.failed("Please fill out password field.");
        }
        
        if (username.isEmpty() && !password.isEmpty()) {
            usernameTextField.grabFocus();
            return ValidationResult.failed("Please fill out username field.");
        }
        return ValidationResult.success();  
    }
    
    private void loginInBackground(String username, String password) {
        new javax.swing.SwingWorker<LoginResult, Void>() {

            @Override
            protected LoginResult doInBackground() {
                AuthenticationService authService = new AuthenticationService();
                return authService.loginUser(username, password);
            }

            @Override
            protected void done() {
                try {
                    LoginResult result = get();
                    afterLogin(result);
                } catch (InterruptedException | ExecutionException e) {
                    logger.log(java.util.logging.Level.SEVERE, "Login error", e);
                    onLoginError();
                }
            }
        }.execute();
    }
    
    private void afterLogin(LoginResult result) {
        hideLoading();
        signInButton.setText("Sign in");

        if (result.isSuccess()) {
            onLoginSuccess(result.getUser());
        } else {
            onLoginFailed(result.getMessage());
        }
    }

    private void onLoginSuccess(User user) {
        hideMessage();
        passwordTextField.setText("");
        System.out.println("User: " + user.getUsername() + " successfully logged in.");
        this.dispose();
    }

    private void onLoginFailed(String errorMessage) {
        showMessage(errorMessage, "error");
        passwordTextField.setText("");
        passwordTextField.grabFocus();
    }

    private void onLoginError() {
        hideLoading();
        signInButton.setEnabled(true);
        signInButton.setText("Sign in");
        showMessage("Something went wrong. Please try again.", "error");
        passwordTextField.setText("");
        passwordTextField.grabFocus();
    }
